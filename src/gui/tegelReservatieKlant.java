    private void btnRerservatieLijstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRerservatieLijstActionPerformed
        // Reservaties van orders
        Klanten klant = lstGevondenKlanten.getSelectedValue();
        List<Artikelen> artikelen = lstArtikelenLijst.getSelectedValuesList();
        if (klant == null || artikelen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Reservatie niet gemaakt. Klant,Artikelen zijn verplicht te selecteren", "NietGelukt", JOptionPane.PLAIN_MESSAGE);
        } else {
            List<Artikelen> artikelList = lstArtikelenLijst.getSelectedValuesList();
            for (Artikelen artikel : artikelList) {
                String aantalText = txtAaantal.getText();
                try {
                    int aantal = Integer.parseInt(aantalText);

                    Orderlijnen orderlijn = new Orderlijnen();
                    orderlijn.setAantal(aantal);
                    orderlijn.setArtikel(artikel);
                    orderlijn.setOrders(order);

                    orderlijn.setPrijs(artikel.getPrijs());

                    if (rdbGeleverd.isSelected()) {
                        orderlijn.setStatus(EStatus.geleverd);
                    } else if (rdbNietGeleverd.isSelected()) {
                        orderlijn.setStatus(EStatus.nietgeleverd);
                    }

                    boolean items = order.getOrderlijnenList().add(orderlijn);
                    LOG.info(items);
                    LOG.info("Orderlijst" + order.getOrderlijnenList().toString());

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Klant, Artikel, en Aantal zijn verplicht te selecteren!", "Melding", JOptionPane.PLAIN_MESSAGE);
                }
            }
            JOptionPane.showMessageDialog(this, "Reservatie aangemaakt en toegevoegd in winkelmandje.", "Gelukt", JOptionPane.PLAIN_MESSAGE);

            txtAaantal.setText(null);
        }
    }//GEN-LAST:event_btnRerservatieLijstActionPerformed

