    private void clearVelden() {
        txfNaam.setText(null);
        txtAaantal.setText(null);
        txfEmail.setText(null);
        txfTelefoon.setText(null);
        txfArtikelToevoegen.setText(null);
        txfArtikelOmschrijving.setText(null);

        int klantIndex = lstGevondenKlanten.getFirstVisibleIndex();
        if (klantIndex != -1) {
            mdlKlanten.remove(klantIndex);
            mdlKlanten.clear();
        }
        int artikelIndex = lstArtikelenLijst.getFirstVisibleIndex();
        if (artikelIndex != -1) {
            mdlArtikelen.remove(artikelIndex);
            mdlArtikelen.clear();
        }
        lblKlantMessage.setText(null);
    }

