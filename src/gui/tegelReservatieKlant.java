    private void btnOpzoekenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpzoekenActionPerformed
        String klantnaam = txfNaam.getText();
        String gsm = txfTelefoon.getText();
        String email = txfEmail.getText();

        KlantenDAO k = new KlantenDAO();
        List<Klanten> resultaat = new ArrayList();
        System.out.println(klantnaam + ", " + gsm + " ," + email);
        if (!klantnaam.equals("")) {
            lblKlantMessage.setText(null);
            resultaat = k.zoekKlanten(klantnaam);
        } else if (!gsm.equals("")) {
            lblKlantMessage.setText(null);
            resultaat = k.zoekKlantenOpGsm(gsm);
        } else if (!email.equals("")) {
            lblKlantMessage.setText(null);
            resultaat = k.zoekKlantenOpEmail(email);
        } else {
            lblKlantMessage.setText("De opzoek velden mogen niet leeg zijn! Gelieve een van de velden in te vullen.");
        }
        System.out.println(resultaat);
        mdlKlanten = new DefaultListModel();
        for (Klanten klant : resultaat) {
            mdlKlanten.addElement(klant);
        }
        lstGevondenKlanten.setModel(mdlKlanten);

    }//GEN-LAST:event_btnOpzoekenActionPerformed

